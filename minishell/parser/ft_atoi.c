#include "parser.h"

int	ft_atoi(char *str)
{
	int		i;
	long	r;
	int		sn;

	r = 0;
	i = 0;
	sn = 1;
	if (str[i] == '-')
		sn *= -1;
	if (str[i] == '+')
		i++;
	if (!str[i])
		return (-1);
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = (r * 10) + str[i] - '0';
		i++;
	}
	return (r * sn);
}